// 카마이클 수
//  카마이클 수는 페르마 소정리를 모든 밑에 대해 만족하지만 합성수인 수

#include <bits/stdc++.h>
#define FIO ios::sync_with_stdio(0), cin.tie(0), cout.tie(0);
using namespace std;

int main()
{
    FIO;
    // 반례: n = 54519328481, 소인수 m = 503
    cout << "54519328481 503";
    return 0;
}

